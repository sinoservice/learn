package com.easyhome.vo.dms;

public class AppVersionBean {
	/**
	 * OMS技师id
	 */
	private String omsMasterId;
	/**
	 * Android版本
	 */
	private String androidVersionNo;
	/**
	 * app版本号
	 */
	private String appVersionNo;
	/**
	 * 手机型号
	 */
	private String phoneModel;
	/**
	 * 技师电话
	 */
	private String masterPhone;
	/**
	 * 手机唯一标识
	 */
	private String phoneImei;
	/**
	 * 创建人
	 */
	private String creator;
	/**
	 * 修改人
	 */
	private String modifier;
	public String getOmsMasterId() {
		return omsMasterId;
	}
	public void setOmsMasterId(String omsMasterId) {
		this.omsMasterId = omsMasterId;
	}
	public String getAndroidVersionNo() {
		return androidVersionNo;
	}
	public void setAndroidVersionNo(String androidVersionNo) {
		this.androidVersionNo = androidVersionNo;
	}
	public String getAppVersionNo() {
		return appVersionNo;
	}
	public void setAppVersionNo(String appVersionNo) {
		this.appVersionNo = appVersionNo;
	}
	public String getPhoneModel() {
		return phoneModel;
	}
	public void setPhoneModel(String phoneModel) {
		this.phoneModel = phoneModel;
	}
	public String getMasterPhone() {
		return masterPhone;
	}
	public void setMasterPhone(String masterPhone) {
		this.masterPhone = masterPhone;
	}
	public String getPhoneImei() {
		return phoneImei;
	}
	public void setPhoneImei(String phoneImei) {
		this.phoneImei = phoneImei;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getModifier() {
		return modifier;
	}
	public void setModifier(String modifier) {
		this.modifier = modifier;
	}
	
}
