package com.easyhome.po.oms;

public class InstallNodeSaveRequest {
	private String etdlno;
	private String skuid;
	private String emincode;
	private String groupcode;
	private String installindex;
	private String picturecontent;
	
	public String getEtdlno() {
		return etdlno;
	}
	public void setEtdlno(String etdlno) {
		this.etdlno = etdlno;
	}
	public String getSkuid() {
		return skuid;
	}
	public void setSkuid(String skuid) {
		this.skuid = skuid;
	}
	public String getEmincode() {
		return emincode;
	}
	public void setEmincode(String emincode) {
		this.emincode = emincode;
	}
	public String getInstallindex() {
		return installindex;
	}
	public void setInstallindex(String installindex) {
		this.installindex = installindex;
	}
	public String getPicturecontent() {
		return picturecontent;
	}
	public void setPicturecontent(String picturecontent) {
		this.picturecontent = picturecontent;
	}
	public String getGroupcode() {
		return groupcode;
	}
	public void setGroupcode(String groupcode) {
		this.groupcode = groupcode;
	}
}
