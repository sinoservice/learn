package com.easyhome.vo.dms;

public class ServiceAreaRequest {

	private String ebplType;
	
	private String ebplParentPmCode;
	
	private String ebplCode;

	public String getEbplType() {
		return ebplType;
	}

	public void setEbplType(String ebplType) {
		this.ebplType = ebplType;
	}

	public String getEbplParentPmCode() {
		return ebplParentPmCode;
	}

	public void setEbplParentPmCode(String ebplParentPmCode) {
		this.ebplParentPmCode = ebplParentPmCode;
	}

	public String getEbplCode() {
		return ebplCode;
	}

	public void setEbplCode(String ebplCode) {
		this.ebplCode = ebplCode;
	}
}
